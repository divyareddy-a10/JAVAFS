package DAY3;

public class personDetails {
private String name;
private float age;


public personDetails(String name, float age) {
	
	this.name = name;
	this.age = age;
}

public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public float getAge() {
	return age;
}
public void setAge(float age) {
	this.age = age;
}

}
